    @Override
    public void initView() {
        jTable1.setModel(this.model);
        jTable1.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                linhaClicadaParaAtualizacao = jTable1.rowAtPoint(evt.getPoint());
                PedidoTb tipo = model.getModel(linhaClicadaParaAtualizacao);
                setForm(tipo);
            }
        });

        java.awt.EventQueue.invokeLater(() ->  this.setVisible(true));
    }

    @Override
    public void list(List<PedidoTb> models) {
        this.element = null;
        this.model.limpaTabela();
        models.forEach(tipo -> {
            this.model.insert(tipo);
        });
    }

