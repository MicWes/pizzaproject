    @Override
    public PedidoTb getModel() {
        return this.model.getModel(this.linhaClicadaParaAtualizacao);
    }

    @Override
    public void updateModel(PedidoTb pedido) {
        this.model.fireTableRowsUpdated(this.linhaClicadaParaAtualizacao, this.linhaClicadaParaAtualizacao);
    }

    @Override
    public void insertModel(PedidoTb pedido) {
        this.model.insert(pedido);
    }

    @Override
    public void deleteModel(List<PedidoTb> pedidos) {
        this.element = null;
        this.model.removeModels(pedidos);
    }

    @Override
    public List<PedidoTb> getModels() {
        int[] linhasSelecionadas = jTable1.getSelectedRows();
        List<PedidoTb> listaExcluir = new ArrayList();
        for (int i = 0; i < linhasSelecionadas.length; i++) {
            PedidoTb tipo = this.model.getModel(linhasSelecionadas[i]);
            listaExcluir.add(tipo);
        }
        return listaExcluir;
    }

    @Override
    public void setController(PedidoTbJpaController controller) {
        this.controller = controller;
    }
